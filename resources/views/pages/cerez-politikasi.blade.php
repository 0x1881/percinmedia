                        <h3>Analitik çerezler</h3>
                        <p>Analitik çerezler,
                            kullanıcıların davranışlarının analiz edilebilmesi amacıyla istatistiki ölçümler yapılmasına
                            olanak tanıyan
                            çerezlerdir. Bu çerezler, İnternet sitesinin performansının ve sunulan hizmetin kalitesinin
                            artırılması amaçlarıyla
                            kullanılmaktadır.</p>
                        <h3>Reklam Çerezleri</h3>
                        <p>Reklam çerezleri, İnternet sitesi aracılığıyla reklam ortaklarının
                            yerleştirdikleri çerezler olup, sorumlulukları yerleştiren üçüncü taraflara aittir. Bu
                            çerezler, üçüncü taraflarca
                            kullanıcıların ilgi alanlarına göre profillerinin çıkarılması ve uygun reklam içeriklerinin
                            belirlenerek
                            gösterilmesi amaçlarıyla kullanılmaktadır.</p>
                        <h3>Yerel depolama</h3>
                        <p>Yerel depolama, İnternet tarayıcılarında
                            veri saklamak için kullanılan bir teknolojidir. Bu sistem, kullanıcıların tercihlerini
                            hatırlamak, oturum
                            bilgilerini saklamak ve İnternet sitesinin performansını artırmak amaçlarıyla
                            kullanılmaktadır. Çerezlerden farklı
                            olarak, bu veriler otomatik olarak sunucuya gönderilmez.</p>
                        <h3>Zorunlu Çerezler</h3>
                        <p>Zorunlu çerezler, bir
