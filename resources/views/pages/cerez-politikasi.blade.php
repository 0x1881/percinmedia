                            çerezler yer almaktadır.</p>
                        <h2>Çerez Nedir?</h2>
                        <p>Çerezler, bir İnternet sayfası ziyaret edildiğinde kullanıcılara
                            ilişkin birtakım bilgilerin kullanıcıların terminal cihazlarında depolanmasına izin veren
                            düşük boyutlu zengin metin
                            biçimli text formatlarıdır. Çerezler, bir İnternet sitesini ilk ziyaretiniz sırasında
                            tarayıcınız aracılığıyla
                            cihazınıza depolanabilir ve aynı siteyi aynı cihazla tekrar ziyaret ettiğinizde, tarayıcınız
                            cihazınızda site adına
                            kayıtlı bir çerez olup olmadığını kontrol eder. Eğer kayıt var ise, kaydın içindeki veriyi
                            ziyaret etmekte olduğunuz
                            İnternet sitesine iletir. Bu sayede İnternet sitesi, sizin daha önceki ziyaretinizi tespit
                            eder ve size iletilecek
                            içeriği ona göre belirler.</p>
                        <h2>Çerezler Neden Kullanılır?</h2>
                        <p>Çerezler, İnternet sitesinin daha verimli
                            çalışması ve kullanıcılarının kişisel ihtiyaçlarına daha uygun hizmet sunulması amaçlarıyla
                            kullanılmaktadır.
                            Kullanmakta olduğumuz çerezler, bilgilerin toplanması, İnternet sitemizin işlevselliğini ve
                            performansını
                            artırılması, kullanıcı deneyiminin iyileştirilmesi ve yasal yükümlülüklerin yerine
                            getirilmesi gibi işlevleri yerine
                            getirmektedir. Bunların yanı sıra, kullanmakta olduğumuz çerezler sayesinde:</p>
                        <ul>
                            <li>Oturum bilgileriniz korunur ve güvenliğiniz sağlanır.</li>
                            <li>Site içi deneyiminiz iyileştirilir ve hızlandırılır.</li>
                            <li>Tercihleriniz hatırlanarak size özel içerikler sunulur.</li>
                            <li>Site kullanımına ilişkin analiz yapılarak hizmet kalitesi artırılır.</li>
                        </ul>
